    def identify_outliers(self, df, columns, method='iqr', multiplier=3):
        """Identify outliers using IQR or Z-score method."""
        outliers = {}
        
        for col in columns:
            if col not in df.columns:
                continue
                
            valid_data = df[col].dropna()
            if len(valid_data) == 0:
                continue
                
            if method == 'iqr':
                Q1 = valid_data.quantile(0.25)
                Q3 = valid_data.quantile(0.75)
                IQR = Q3 - Q1
                lower_bound = Q1 - multiplier * IQR
                upper_bound = Q3 + multiplier * IQR
                outlier_mask = (df[col] < lower_bound) | (df[col] > upper_bound)
            else:  # z-score
                mean = valid_data.mean()
                std = valid_data.std()
                z_scores = np.abs((df[col] - mean) / std)
                outlier_mask = z_scores > multiplier
            
            outlier_indices = df[outlier_mask].index
            if len(outlier_indices) > 0:
                outliers[col] = {
                    'indices': outlier_indices,
                    'values': df.loc[outlier_indices, col].tolist(),
                    'filenames': df.loc[outlier_indices, 'filename'].tolist() if 'filename' in df.columns else [],
                    'pdb_ids': df.loc[outlier_indices, 'pdb_id'].tolist() if 'pdb_id' in df.columns else []
                }
        
        return outliers
        
    def generate_outlier_report(self, table_name, outliers):
        """Generate a detailed outlier report."""
        report_file = self.output_dir / f"{table_name}_outlier_report.txt"
        
        with open(report_file, 'w') as f:
            f.write(f"Outlier Analysis Report for {table_name}\n")
            f.write("="*60 + "\n\n")
            
            if not outliers:
                f.write("No outliers detected.\n")
                return
            
            for col, data in outliers.items():
                f.write(f"Column: {col}\n")
                f.write(f"Number of outliers: {len(data['indices'])}\n")
                f.write(f"Outlier values: {data['values']}\n")
                if data['pdb_ids']:
                    f.write(f"PDB IDs: {data['pdb_ids']}\n")
                if data['filenames']:
                    f.write(f"Filenames: {data['filenames']}\n")
                f.write("\n" + "-"*40 + "\n\n")
        
        print(f"Outlier report saved: {report_file}")
        return outliers
        
    def remove_outliers_from_data(self, df, outliers):
        """Remove outliers from dataset and return cleaned data."""
        if not outliers:
            return df
            
        # Collect all outlier indices
        all_outlier_indices = set()
        for col_data in outliers.values():
            all_outlier_indices.update(col_data['indices'])
        
        # Remove outliers
        cleaned_df = df.drop(index=list(all_outlier_indices)).reset_index(drop=True)
        print(f"Removed {len(all_outlier_indices)} outlier samples from dataset")
        
        return cleaned_df
        
