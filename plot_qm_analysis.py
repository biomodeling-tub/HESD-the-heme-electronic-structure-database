    def plot_correlation_matrix(self):
        """Create correlation matrices for numerical data across all tables."""
        fig, axes = plt.subplots(2, 2, figsize=(20, 16))
        
        table_names = ['fe_coordination_analysis', 'orbital_analysis', 'charge_transfer_analysis', 'spin_contamination_analysis']
        
        for i, table_name in enumerate(table_names):
            if table_name in self.tables:
                df = self.tables[table_name]
                # Select only numerical columns
                numerical_cols = df.select_dtypes(include=[np.number]).columns
                if len(numerical_cols) > 1:
                    corr_matrix = df[numerical_cols].corr()
                    
                    row, col = i // 2, i % 2
                    im = axes[row, col].imshow(corr_matrix.values, cmap='RdBu_r', vmin=-1, vmax=1)
                    
                    # Add correlation values as text
                    for j in range(len(corr_matrix.columns)):
                        for k in range(len(corr_matrix.columns)):
                            text = axes[row, col].text(k, j, f'{corr_matrix.iloc[j, k]:.2f}',
                                                     ha="center", va="center", color="black", fontsize=8)
                    
                    axes[row, col].set_xticks(range(len(corr_matrix.columns)))
                    axes[row, col].set_yticks(range(len(corr_matrix.columns)))
                    axes[row, col].set_xticklabels(corr_matrix.columns, rotation=45, ha='right')
                    axes[row, col].set_yticklabels(corr_matrix.columns)
                    
                    # Add colorbar
                    cbar = plt.colorbar(im, ax=axes[row, col])
                    cbar.set_label('Correlation', rotation=270, labelpad=15)
                else:
                    row, col = i // 2, i % 2
                    axes[row, col].text(0.5, 0.5, f'Insufficient numerical data\n{table_name}', 
                                      ha='center', va='center')
            else:
                row, col = i // 2, i % 2
                axes[row, col].text(0.5, 0.5, f'Table not found\n{table_name}', ha='center', va='center')
        
        plt.tight_layout()
        plt.savefig(self.output_dir / 'correlation_matrices.png', dpi=300, bbox_inches='tight')
        plt.close()
        print("Correlation matrices saved")
        
    def generate_summary_report(self):
        """Generate a summary report of all loaded tables."""
        summary_file = self.output_dir / 'data_summary.txt'
        
        with open(summary_file, 'w') as f:
            f.write("Quantum Chemistry Analysis Tables Summary\n")
            f.write("="*50 + "\n\n")
            
            for table_name, df in self.tables.items():
                f.write(f"Table: {table_name}\n")
                f.write(f"Rows: {len(df)}\n")
                f.write(f"Columns: {len(df.columns)}\n")
                f.write(f"Numerical columns: {len(df.select_dtypes(include=[np.number]).columns)}\n")
                f.write(f"Non-null percentage: {(df.notna().sum().sum() / (len(df) * len(df.columns)) * 100):.1f}%\n")
                f.write("\nColumn summary:\n")
                
                for col in df.columns:
                    non_null_count = df[col].notna().sum()
                    f.write(f"  {col}: {non_null_count}/{len(df)} non-null ({non_null_count/len(df)*100:.1f}%)\n")
                
                f.write("\n" + "-"*40 + "\n\n")
        
        print(f"Summary report saved to {summary_file}")
        
