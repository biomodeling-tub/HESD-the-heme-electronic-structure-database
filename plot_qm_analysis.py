    def merge_with_auxiliary(self, df):
        """Merge dataframe with auxiliary charge/multiplicity/axial data."""
        if self.auxiliary_data.empty or 'filename' not in df.columns:
            return df
            
        # Create mapping between filenames
        df_copy = df.copy()
        df_copy['base_filename'] = df_copy['filename'].str.replace('.json', '.log')
        
        # Merge based on filename
        merged = df_copy.merge(self.auxiliary_data, left_on='base_filename', right_on='file_name', how='left')
        
        # Create charge-multiplicity combination if both exist
        charge_cols = [col for col in merged.columns if 'charge' in col.lower()]
        mult_cols = [col for col in merged.columns if 'multiplicity' in col.lower()]
        
        if charge_cols and mult_cols:
            charge_col = charge_cols[0]
            mult_col = mult_cols[0]
            # Only create charge_multiplicity for rows where both values are not null
            merged['charge_multiplicity'] = merged.apply(
                lambda row: f"{row[charge_col]},{row[mult_col]}" 
                if pd.notna(row[charge_col]) and pd.notna(row[mult_col]) 
                else None, axis=1
            )
            
        return merged
    
